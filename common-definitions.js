// Cache for fetched definitions to avoid repeated API calls
const definitionCache = new Map();

// Function to get definition from Free Dictionary API
async function fetchDefinitionFromAPI(word) {
  // Try Datamuse first (best success rate)
  try {
    const response = await fetch(`https://api.datamuse.com/words?sp=${word.toLowerCase()}&md=d&max=1`);
    const data = await response.json();
    
    if (data && data[0] && data[0].defs && data[0].defs[0]) {
      // Datamuse format: "part_of_speech\tdefinition"
      const defParts = data[0].defs[0].split('\t');
      let definition = defParts.length > 1 ? defParts[1] : defParts[0];
      
      // Clean up the definition
      definition = definition.charAt(0).toUpperCase() + definition.slice(1);
      if (!definition.endsWith('.')) {
        definition += '.';
      }
      
      return definition;
    }
  } catch (error) {
    console.warn(`Datamuse failed for "${word}":`, error.message);
  }
  
  // Fallback to FreeDictionary
  try {
    const response = await fetch(`https://api.dictionaryapi.dev/api/v2/entries/en/${word.toLowerCase()}`);
    if (!response.ok) {
      throw new Error(`HTTP error! status: ${response.status}`);
    }
    
    const data = await response.json();
    if (data && data.length > 0 && data[0].meanings && data[0].meanings.length > 0) {
      const firstMeaning = data[0].meanings[0];
      if (firstMeaning.definitions && firstMeaning.definitions.length > 0) {
        let definition = firstMeaning.definitions[0].definition;
        
        // Clean up the definition
        definition = definition.charAt(0).toUpperCase() + definition.slice(1);
        if (!definition.endsWith('.')) {
          definition += '.';
        }
        
        return definition;
      }
    }
  } catch (error) {
    console.warn(`FreeDictionary failed for "${word}":`, error.message);
  }
  
  return null;
}

